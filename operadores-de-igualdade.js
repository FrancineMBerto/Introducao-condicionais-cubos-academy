// dois iguais (==) é uma pergunta! Já um igual só (=) é uma afirmação!

let x = "18";
let y = 19;

const ehIgualTrue = (18 == x); //true (Retorna verdadeiro caso os operandos sejam iguais (independente do tipo)) Igual == 
console.log(ehIgualTrue)

const ehIgualFalse = (x == y); //false
console.log(ehIgualFalse);

const ehDiferenteTrue = (y != x); //true (Retorna verdadeiro caso os operandos não sejam iguais) Diferente !=
console.log(ehDiferenteTrue);

const ehDiferenteFalse = (y != y); //false
console.log(ehDiferenteFalse);




