// Aula 02 Operadores de Comparação.

const ehMaior = 25 > 16; //true (Retorna verdadeiro caso o operando da esquerda seja maior que o da direita) Maior que >  
console.log(ehMaior);

const ehMenor = 15 < 14; //false (Retorna falso caso o operando da direita seja menor que o da esquerda) Menor que <
console.log(ehMenor);

const ehMaiorOuIgual = 15 >= 15; //true (Retorna verdadeiro caso o operando da esquerda seja maior ou igual o da direita) Maior ou igual >=
console.log(ehMaiorOuIgual);

const ehMenorOuIgual = 15 <= 13; //false (Retorna falso caso o operando da direita seja menor ou igual o da esquerda) Menor ou igual <=
console.log(ehMenorOuIgual);

// podemos fazer a comparação com variáveis atribuidas ...

const x = 8;
const y = 16;
const ehTrue = x <= y; // true
console.log(ehTrue);


const z = 30;
const v = 10;
const ehFalse = z <= v; // false
console.log(ehFalse);









